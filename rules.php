					  <li>Ball may be served underhand or overhand.</li>
					  <li>Served ball may graze the net and drop to the other side for point</li>
					  <li>First game serve is determined by a volley. Minimum 3 rallies.</li>
					  <li>Serve must be returned by bump or volley. No attacking or blocking a serve.</li>
