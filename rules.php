                  <ul>
					<li>Point on serve scoring will be used.</li>
					<li>Point scored will only count if the team has serve.</li>
					<li>Offense will score on a defense miss or out of bounds hit.</li>
					<li>Group Games will be played to 15 points (Quarter-Finals &amp; Semi-Finals 21 Points).</li>
					<li>Finals will be played to 15 points in a Best of 3 Set.</li>
					<li>Games tied at 15 points must win by 2 point difference.</li>
				</ul>
                </div>
				<div class="tab-pane fade" id="rotation">
                  <ul>
					<li>Team will rotate each time they win the serve.</li>
					<li>Players shall rotate in a clockwise manner.</li>
					<li>There shall be a minimum of 5 players on each side.</li>
				</ul>
                </div>
				<div class="tab-pane fade" id="volleying">
                  <ul>
					<li>Maximum of three hits per side. A block is not considered a hit.</li>
					<li>Player may not hit the ball twice in succession ( A block is not considered a hit ).</li>
					<li>Ball may be played off the net during a volley and on serve.</li>
					<li>A ball touching a boundary line is good.</li>
					<li>A legal hit is contact with the ball by a player body above and including the waist which does not allow the ball to visibly come to a rest. Ball may hit below waist and hit foot or leg but cannot be done so in a kicking motion</li>
					<li>If two or more players contact the ball simultaneously, it is considered one play and the players involved may not participate in the next play.</li>
					<li>A player must not block or attack a serve.</li>
					<li>Switching positions will be allowed only between front line players. You must return to your original position after the point and proceed with normal rotation.</li>
				  </ul>
